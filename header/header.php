<script>
  document.addEventListener('DOMContentLoaded', () => {
    const menuToggle = document.getElementById('menuToggle');
    const mobileMenu = document.getElementById('mobileMenu');
    const menuIcon = document.getElementById('menuIcon');

    let isOpen = false;

    // Toggle mobile menu
    menuToggle.addEventListener('click', (e) => {
      e.stopPropagation();
      isOpen = !isOpen;
      mobileMenu.style.display = isOpen ? 'flex' : 'none';
      // Fixed: Only show three lines in the hamburger icon
      menuIcon.innerHTML = isOpen
        ? `<path d="M6 6L18 18M6 18L18 6" stroke="#333" stroke-width="2" stroke-linecap="round"/>`
        : `<path d="M3 6H21M3 12H21M3 18H21" stroke="#333" stroke-width="2" stroke-linecap="round"/>`;
    });

    // Close mobile menu when clicking outside
    document.addEventListener('click', (e) => {
      if (isOpen && !mobileMenu.contains(e.target) && !menuToggle.contains(e.target)) {
        isOpen = false;
        mobileMenu.style.display = 'none';
        menuIcon.innerHTML = `<path d="M3 6H21M3 12H21M3 18H21" stroke="#333" stroke-width="2" stroke-linecap="round"/>`;
      }
    });

    // Prevent mobile menu from closing when clicking inside it
    mobileMenu.addEventListener('click', (e) => {
      e.stopPropagation();
    });

    // Highlight current page for mobile menu
    const currentPage = window.location.pathname.split('/').pop() || 'index.php';
    const mobileLinks = document.querySelectorAll('.mobile-menu a');
    mobileLinks.forEach(link => {
      const href = link.getAttribute('href');
      if (href && href.includes(currentPage)) {
        link.classList.add('active');
      }
    });
  });
</script>
